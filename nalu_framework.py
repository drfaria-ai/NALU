    per_label_df.to_csv(per_label_csv, index=False, encoding="utf-8")
    per_label_md_saved = False
    try:
      # Optional Markdown export; do not crash if `tabulate` is missing.
      per_label_df.to_markdown(per_label_md, index=False)
      per_label_md_saved = True
    except Exception as exc:
      (report_dir / "markdown_export_error_per_label.txt").write_text(str(exc), encoding="utf-8")

