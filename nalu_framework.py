@dataclass
class NALUSchema:
  """Schema and grouped heads for the NALU dataset."""

  id_col: str = "sample_id"
  text_col: str = "text"

  regression_cols: List[str] = field(default_factory=lambda: [
    "valence_russell",
    "arousal_russell",
  ])

  # NALU-PTBR: psychometric regression targets.
  # Values are normalized to [0, 1] in the NALU-PTBR dataset.
  # The psychometric_mask column indicates rows with valid psychometric supervision.
  psychometric_cols: List[str] = field(default_factory=lambda: [
    "phq9_score_norm",
    "gad7_score_norm",
    "dass_depression_norm",
    "dass_anxiety_norm",
    "dass_stress_norm",
  ])

  psychometric_mask_col: str = "psychometric_mask"

  sentiment_cols: List[str] = field(default_factory=lambda: [
    "sentiment_positive",
    "sentiment_neutral",
    "sentiment_negative",
  ])

  emotion_cols: List[str] = field(default_factory=lambda: [
    "emotion_joy",
    "emotion_sadness",
    "emotion_anger",
    "emotion_fear",
    "emotion_disgust",
    "emotion_surprise",
    "emotion_calm",
    "emotion_distress",
    "emotion_neutral",
  ])

  psychological_cols: List[str] = field(default_factory=lambda: [
    "stress_pressure_cue",
    "hopelessness_cue",
    "self_devaluation_cue",
    "loss_of_control_cue",
    "social_withdrawal_cue",
    "irritability_cue",
    "cognitive_overload_cue",
    "guilt_shame_cue",
    "resilience_coping_cue",
    "self_focus_cue",
    "empathy_cue",
  ])

  behavioral_cols: List[str] = field(default_factory=lambda: [
    "hostility",
    "toxicity",
    "seeking_help_intent",
    "avoidance_intent",
    "coping_intent",
    "aggressive_intent",
  ])

  risk_cols: List[str] = field(default_factory=lambda: [
    "anxiety_related_language",
    "depression_related_language",
    "crisis_danger_text",
    "mental_state_risk_flag",
  ])

  clinical_cols: List[str] = field(default_factory=lambda: [
    "grief_loss_language",
    "sleep_disturbance_cue",
    "relationship_distress_cue",
    "caregiver_burden_cue",
    "panic_attack_language",
  ])

  @property
  def all_label_cols(self) -> List[str]:
    """All training label columns except the text and id."""
    return (
      self.regression_cols
      + self.psychometric_cols
      + self.sentiment_cols
      + self.emotion_cols
      + self.psychological_cols
      + self.behavioral_cols
      + self.risk_cols
      + self.clinical_cols
    )

  @property
  def all_heads(self) -> Dict[str, List[str]]:
    """Grouped label heads."""
    return {
      "sentiment": self.sentiment_cols,
      "emotion": self.emotion_cols,
      "psychological": self.psychological_cols,
      "behavioral": self.behavioral_cols,
      "risk": self.risk_cols,
      "clinical": self.clinical_cols,
      "regression": self.regression_cols,
      "psychometric": self.psychometric_cols,
    }

  @property
  def multilabel_heads(self) -> Dict[str, List[str]]:
    """Heads treated as sigmoid multi-label."""
    return {
      "emotion": self.emotion_cols,
      "psychological": self.psychological_cols,
      "behavioral": self.behavioral_cols,
      "risk": self.risk_cols,
      "clinical": self.clinical_cols,
    }


