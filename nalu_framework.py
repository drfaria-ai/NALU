class MultiHeadBertimbau(nn.Module):
  """Shared BERT encoder with task-specific heads."""

  def __init__(self, model_name: str, schema: NALUSchema, dropout: float = 0.2, gradient_checkpointing: bool = False):
    super().__init__()
    self.schema = schema
    self.config = AutoConfig.from_pretrained(model_name)
    self.encoder = AutoModel.from_pretrained(model_name, config=self.config)

    if gradient_checkpointing and hasattr(self.encoder, "gradient_checkpointing_enable"):
      self.encoder.gradient_checkpointing_enable()

    hidden_size = self.config.hidden_size
    self.dropout = nn.Dropout(dropout)

    self.sentiment_head = nn.Sequential(
      nn.Linear(hidden_size, hidden_size),
      nn.GELU(),
      nn.Dropout(dropout),
      nn.Linear(hidden_size, len(schema.sentiment_cols)),
    )

    self.emotion_head = nn.Sequential(
      nn.Linear(hidden_size, hidden_size),
      nn.GELU(),
      nn.Dropout(dropout),
      nn.Linear(hidden_size, len(schema.emotion_cols)),
    )

    self.psychological_head = nn.Sequential(
      nn.Linear(hidden_size, hidden_size),
      nn.GELU(),
      nn.Dropout(dropout),
      nn.Linear(hidden_size, len(schema.psychological_cols)),
    )

    self.behavioral_head = nn.Sequential(
      nn.Linear(hidden_size, hidden_size),
      nn.GELU(),
      nn.Dropout(dropout),
      nn.Linear(hidden_size, len(schema.behavioral_cols)),
    )

    self.risk_head = nn.Sequential(
      nn.Linear(hidden_size, hidden_size),
      nn.GELU(),
      nn.Dropout(dropout),
      nn.Linear(hidden_size, len(schema.risk_cols)),
    )

    self.clinical_head = nn.Sequential(
      nn.Linear(hidden_size, hidden_size),
      nn.GELU(),
      nn.Dropout(dropout),
      nn.Linear(hidden_size, len(schema.clinical_cols)),
    )

    self.regression_head = nn.Sequential(
      nn.Linear(hidden_size, hidden_size),
      nn.GELU(),
      nn.Dropout(dropout),
      nn.Linear(hidden_size, len(schema.regression_cols)),
      nn.Tanh(), # keep Russell outputs bounded within [-1, 1]
    )

    # NALU psychometric regression head.
    # Sigmoid bounds each normalized psychometric score within [0, 1].
    self.psychometric_head = nn.Sequential(
      nn.Linear(hidden_size, hidden_size),
      nn.GELU(),
      nn.Dropout(dropout),
      nn.Linear(hidden_size, len(schema.psychometric_cols)),
      nn.Sigmoid(),
    )

  def forward(self, input_ids: torch.Tensor, attention_mask: torch.Tensor) -> Dict[str, torch.Tensor]:
    """Forward pass."""
    outputs = self.encoder(input_ids=input_ids, attention_mask=attention_mask)
    if hasattr(outputs, "pooler_output") and outputs.pooler_output is not None:
      pooled = outputs.pooler_output
    else:
      pooled = outputs.last_hidden_state[:, 0]

    pooled = self.dropout(pooled)

    return {
      "sentiment_logits": self.sentiment_head(pooled),
      "emotion_logits": self.emotion_head(pooled),
      "psychological_logits": self.psychological_head(pooled),
      "behavioral_logits": self.behavioral_head(pooled),
      "risk_logits": self.risk_head(pooled),
      "clinical_logits": self.clinical_head(pooled),
      "regression": self.regression_head(pooled),
      "psychometric": self.psychometric_head(pooled),
    }

  def freeze_encoder(self) -> None:
    """Freeze encoder parameters."""
    for p in self.encoder.parameters():
      p.requires_grad = False

  def unfreeze_encoder(self) -> None:
    """Unfreeze encoder parameters."""
    for p in self.encoder.parameters():
      p.requires_grad = True


