class NALUDataModule:
  """Read the dataset, validate schema, split data, and build loaders."""

  def __init__(self, config: TrainConfig, schema: NALUSchema):
    self.config = config
    self.schema = schema
    self.df: Optional[pd.DataFrame] = None
    self.train_df: Optional[pd.DataFrame] = None
    self.val_df: Optional[pd.DataFrame] = None
    self.test_df: Optional[pd.DataFrame] = None

  def load(self) -> pd.DataFrame:
    """Load dataset from CSV and validate columns."""
    resolved_dataset = resolve_dataset_path(self.config.dataset_path)
    self.config.dataset_path = str(resolved_dataset)
    df = pd.read_csv(resolved_dataset)

    # NALU-PTBR uses the canonical schema expected by this framework.
    # If psychometric columns are missing, they are created as zeros and masked out.
    required_non_psychometric = (
      [self.schema.id_col, self.schema.text_col]
      + self.schema.regression_cols
      + self.schema.sentiment_cols
      + self.schema.emotion_cols
      + self.schema.psychological_cols
      + self.schema.behavioral_cols
      + self.schema.risk_cols
      + self.schema.clinical_cols
    )
    missing = [c for c in required_non_psychometric if c not in df.columns]
    if missing:
      raise ValueError(f"Missing required columns in dataset: {missing}")

    for col in self.schema.psychometric_cols:
      if col not in df.columns:
        df[col] = 0.0
    if self.schema.psychometric_mask_col not in df.columns:
      df[self.schema.psychometric_mask_col] = 0.0

    # Keep only relevant columns in canonical order.
    ordered_cols = [self.schema.id_col, self.schema.text_col] + self.schema.all_label_cols + [self.schema.psychometric_mask_col]
    df = df[ordered_cols].copy()

    # Basic cleaning.
    df[self.schema.text_col] = df[self.schema.text_col].astype(str).fillna("").str.strip()
    df = df[df[self.schema.text_col].str.len() > 0].copy()

    # Ensure binary heads are numeric 0/1.
    binary_cols = (
      self.schema.sentiment_cols
      + self.schema.emotion_cols
      + self.schema.psychological_cols
      + self.schema.behavioral_cols
      + self.schema.risk_cols
      + self.schema.clinical_cols
    )
    for col in binary_cols:
      df[col] = pd.to_numeric(df[col], errors="coerce").fillna(0).clip(0, 1).astype(int)

    # Ensure Russell regression columns are floats in [-1, 1].
    for col in self.schema.regression_cols:
      df[col] = pd.to_numeric(df[col], errors="coerce").fillna(0.0).clip(-1.0, 1.0).astype(float)

    # Ensure psychometric regression columns are floats in [0, 1].
    for col in self.schema.psychometric_cols:
      df[col] = pd.to_numeric(df[col], errors="coerce").fillna(0.0).clip(0.0, 1.0).astype(float)

    df[self.schema.psychometric_mask_col] = pd.to_numeric(
      df[self.schema.psychometric_mask_col], errors="coerce"
    ).fillna(0.0).clip(0.0, 1.0).astype(float)

    # For sentiment, enforce one dominant class if data is malformed.
    sentiment_array = df[self.schema.sentiment_cols].values
    sentiment_sum = sentiment_array.sum(axis=1)
    malformed = np.where(sentiment_sum != 1)[0]
    if len(malformed) > 0:
      repaired = []
      for idx in malformed:
        row = sentiment_array[idx]
        if row.sum() <= 0:
          # Default to neutral when missing.
          sentiment_array[idx] = np.array([0, 1, 0], dtype=int)
        else:
          # Convert to one-hot using argmax.
          one_hot = np.zeros_like(row)
          one_hot[np.argmax(row)] = 1
          sentiment_array[idx] = one_hot
        repaired.append(idx)
      df[self.schema.sentiment_cols] = sentiment_array

    self.df = df.reset_index(drop=True)
    return self.df

  def split(self) -> Tuple[pd.DataFrame, pd.DataFrame, pd.DataFrame]:
    """Create train/val/test splits with sentiment stratification."""
    if self.df is None:
      self.load()

    assert self.df is not None
    df = self.df.copy()

    sentiment_idx = np.argmax(df[self.schema.sentiment_cols].values, axis=1)

    train_df, temp_df = train_test_split(
      df,
      test_size=(1.0 - self.config.train_size),
      random_state=self.config.seed,
      stratify=sentiment_idx,
    )

    temp_sentiment_idx = np.argmax(temp_df[self.schema.sentiment_cols].values, axis=1)
    relative_test_size = self.config.test_size / (self.config.val_size + self.config.test_size)

    val_df, test_df = train_test_split(
      temp_df,
      test_size=relative_test_size,
      random_state=self.config.seed,
      stratify=temp_sentiment_idx,
    )

    self.train_df = train_df.reset_index(drop=True)
    self.val_df = val_df.reset_index(drop=True)
    self.test_df = test_df.reset_index(drop=True)
    return self.train_df, self.val_df, self.test_df

  def describe(self) -> Dict[str, Any]:
    """Return a compact description of dataset and label distributions."""
    if self.df is None:
      self.load()
    if self.train_df is None:
      self.split()

    def head_stats(split_df: pd.DataFrame) -> Dict[str, Any]:
      out: Dict[str, Any] = {"n_samples": int(len(split_df))}
      for head_name, cols in self.schema.all_heads.items():
        if head_name in {"regression", "psychometric"}:
          reg = {
            col: {
              "mean": float(split_df[col].mean()),
              "std": float(split_df[col].std()),
              "min": float(split_df[col].min()),
              "max": float(split_df[col].max()),
            }
            for col in cols
          }
          out[head_name] = reg
        elif head_name == "sentiment":
          counts = split_df[cols].sum(axis=0).to_dict()
          out[head_name] = {k: int(v) for k, v in counts.items()}
        else:
          counts = split_df[cols].sum(axis=0).to_dict()
          cardinality = split_df[cols].sum(axis=1)
          out[head_name] = {
            "positive_counts": {k: int(v) for k, v in counts.items()},
            "avg_cardinality": float(cardinality.mean()),
            "median_cardinality": float(cardinality.median()),
            "max_cardinality": int(cardinality.max()),
          }
      return out

    return {
      "full": head_stats(self.df),
      "train": head_stats(self.train_df),
      "val": head_stats(self.val_df),
      "test": head_stats(self.test_df),
    }


