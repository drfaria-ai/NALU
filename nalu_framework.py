class NALUTokenizedDataset(Dataset):
  """Tokenized dataset wrapper for BERT-style input and multi-head labels."""

  def __init__(
    self,
    df: pd.DataFrame,
    tokenizer,
    schema: NALUSchema,
    max_length: int,
  ):
    self.df = df.reset_index(drop=True)
    self.tokenizer = tokenizer
    self.schema = schema
    self.max_length = max_length

  def __len__(self) -> int:
    return len(self.df)

  def __getitem__(self, idx: int) -> Dict[str, Any]:
    row = self.df.iloc[idx]
    text = str(row[self.schema.text_col])

    enc = self.tokenizer(
      text,
      truncation=True,
      padding="max_length",
      max_length=self.max_length,
      return_tensors="pt",
    )

    item = {
      "sample_id": row[self.schema.id_col],
      "text": text,
      "input_ids": enc["input_ids"].squeeze(0),
      "attention_mask": enc["attention_mask"].squeeze(0),
      "sentiment": torch.tensor(np.argmax(row[self.schema.sentiment_cols].values.astype(np.int64)), dtype=torch.long),
      "emotion": torch.tensor(row[self.schema.emotion_cols].values.astype(np.float32), dtype=torch.float32),
      "psychological": torch.tensor(row[self.schema.psychological_cols].values.astype(np.float32), dtype=torch.float32),
      "behavioral": torch.tensor(row[self.schema.behavioral_cols].values.astype(np.float32), dtype=torch.float32),
      "risk": torch.tensor(row[self.schema.risk_cols].values.astype(np.float32), dtype=torch.float32),
      "clinical": torch.tensor(row[self.schema.clinical_cols].values.astype(np.float32), dtype=torch.float32),
      "regression": torch.tensor(row[self.schema.regression_cols].values.astype(np.float32), dtype=torch.float32),
      "psychometric": torch.tensor(row[self.schema.psychometric_cols].values.astype(np.float32), dtype=torch.float32),
      "psychometric_mask": torch.tensor(float(row.get(self.schema.psychometric_mask_col, 0.0)), dtype=torch.float32),
    }
    return item


