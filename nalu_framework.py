def _baseline_extract_main_scores(metrics: Dict[str, Any], model_name: str) -> Dict[str, Any]:
  """Convert full MetricsComputer output into one paper-ready summary row."""
  row: Dict[str, Any] = {"model": model_name}
  row["sentiment_macro_f1"] = metrics.get("sentiment", {}).get("macro_f1", np.nan)
  row["sentiment_accuracy"] = metrics.get("sentiment", {}).get("accuracy", np.nan)
  for head in ["emotion", "psychological", "behavioral", "risk", "clinical"]:
    row[f"{head}_macro_f1"] = metrics.get(head, {}).get("macro_f1", np.nan)
    row[f"{head}_micro_f1"] = metrics.get(head, {}).get("micro_f1", np.nan)
    row[f"{head}_hamming_loss"] = metrics.get(head, {}).get("hamming_loss", np.nan)
  reg = metrics.get("regression", {}) or {}
  for col in ["valence_russell", "arousal_russell"]:
    row[f"{col}_mae"] = reg.get(col, {}).get("mae", np.nan)
    row[f"{col}_rmse"] = reg.get(col, {}).get("rmse", np.nan)
    row[f"{col}_pearson"] = reg.get(col, {}).get("pearson", np.nan)
  psy = metrics.get("psychometric", {}) or {}
  psy_maes = []
  for col, vals in psy.items():
    row[f"{col}_mae"] = vals.get("mae", np.nan)
    row[f"{col}_rmse"] = vals.get("rmse", np.nan)
    row[f"{col}_pearson"] = vals.get("pearson", np.nan)
    if vals.get("mae", None) is not None:
      psy_maes.append(vals.get("mae"))
  row["russell_mean_mae"] = float(np.nanmean([row.get("valence_russell_mae", np.nan), row.get("arousal_russell_mae", np.nan)]))
  row["psychometric_mean_mae"] = float(np.nanmean(psy_maes)) if psy_maes else np.nan
  row["composite_score"] = metrics.get("composite_score", np.nan)
  return row


