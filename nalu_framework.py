def read_texts_from_file(input_file: Union[str, Path], text_column: str = "text") -> List[str]:
  """Read texts from txt/csv/json file."""
  input_file = Path(input_file)
  suffix = input_file.suffix.lower()

  if suffix == ".txt":
    with open(input_file, "r", encoding="utf-8") as f:
      texts = [line.strip() for line in f if line.strip()]
    return texts

  if suffix == ".csv":
    df = pd.read_csv(input_file)
    if text_column not in df.columns:
      raise ValueError(f"Column '{text_column}' not found in CSV.")
    return df[text_column].astype(str).fillna("").tolist()

  if suffix == ".json":
    data = json_load(input_file)
    if isinstance(data, list):
      if all(isinstance(x, str) for x in data):
        return data
      if all(isinstance(x, dict) for x in data):
        return [str(x.get(text_column, "")) for x in data if str(x.get(text_column, "")).strip()]
    raise ValueError("Unsupported JSON structure. Expected list[str] or list[dict].")

  raise ValueError(f"Unsupported file type: {suffix}")


def print_prediction_result(result: Dict[str, Any], show_raw_probs: bool = False) -> None:
  """Pretty-print one prediction result in Portuguese in a cleaner clinical mode."""
  s = result["structured_interpretation"]

  def _print_active_block(title: str, items: List[Dict[str, Any]]) -> None:
    print(f"\n{title}")
    if items:
      for item in items:
        print(f" - {item['label']}: {item['score']:.4f}")
    else:
      print(" - nenhum rótulo acima do limiar clínico")

  print("=" * 90)
  print("Texto:")
  print(result["text"])
  print("-" * 90)

  print("1) Sentiment")
  for k, v in sorted(result["sentiment_probs"].items(), key=lambda x: x[1], reverse=True):
    print(f" - {k}: {v:.4f}")

  _print_active_block("2) Emotion (ativos)", s.get("active_emotions", []))
  _print_active_block("3) Psychological cues (ativos)", s.get("active_psychological", []))
  _print_active_block("4) Behavioral / interpersonal (ativos)", s.get("active_behavioral", []))
  _print_active_block("5) Risk / mental-state (ativos)", s.get("active_risk", []))
  _print_active_block("6) Clinical language cues (ativos)", s.get("active_clinical", []))

  print("\n7) Valence & Arousal")
  print(f" - valence_russell: {result['regression']['valence_russell']:.4f}")
  print(f" - arousal_russell: {result['regression']['arousal_russell']:.4f}")
  print(f" - quadrante: {s['affect_space']['quadrant_interpretation']}")

  print("\nIntegração final")
  print(f"Resumo curto: {s['short_summary_pt']}")
  print(f"Interpretação clínica breve: {s['clinical_interpretation_pt']}")
  print(f"Alerta: [{s['alert']['level'].upper()}] {s['alert']['message']}")

  if s["suggested_focus_pt"]:
    print("Pontos sugeridos para explorar:")
    for item in s["suggested_focus_pt"]:
      print(f" - {item}")

  if show_raw_probs:
    print("\nProbabilidades brutas (debug)")
    for block_name, block in [
      ("emotion", result["emotion_probs"]),
      ("psychological", result["psychological_probs"]),
      ("behavioral", result["behavioral_probs"]),
      ("risk", result["risk_probs"]),
      ("clinical", result.get("clinical_probs", {})),
    ]:
      print(f"\n{block_name}:")
      for k, v in sorted(block.items(), key=lambda x: x[1], reverse=True):
        print(f" - {k}: {v:.4f}")

  print("\nComentário final")
  print(result["llm_final_comment_pt"])
  print("\nAviso")
  print(s["non_diagnostic_notice_pt"])
  print("=" * 90)

