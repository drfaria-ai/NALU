

"""
NALU-PTBR: Neuro-Affective Language Understanding Framework
===========================================================

Single-file Python framework for:

1) Training a multi-head BERTimbau model on the NALU-PTBR dataset.
2) Evaluating all heads on train/val/test splits.
3) Producing structured psychological interpretations in Portuguese.
4) Optionally using an LLM as a final rewriter layer.
5) Running offline batch inference for future multimodal integration.
6) Running paper-ready ablations, threshold comparisons, bootstrap CIs and statistical tests.

Authoring assumptions:
- Dataset file is available in the current folder (root) by default.
- Main dataset file: NALU-PTBR.csv
- Comments are in English as requested.
- Program outputs and interpretations are in Portuguese.

Recommended environment:
- Python 3.10+
- torch
- transformers
- pandas
- numpy
- scikit-learn
- tqdm

Example usage
-------------

Train + evaluate:
python nalu_framework.py train \
 --dataset ./NALU-PTBR.csv \
 --output_dir ./nalu_runs/run_001 \
 --epochs 4 \
 --batch_size 16 \
 --max_length 160

Predict a few texts using a trained checkpoint:
python nalu_framework.py predict \
 --checkpoint_dir ./nalu_runs/run_001 \
 --texts "Eu não aguento mais tanta pressão." "Hoje foi difícil, mas estou tentando lidar melhor."

Predict from a txt/csv/json file:
python nalu_framework.py predict_file \
 --checkpoint_dir ./nalu_runs/run_001 \
 --input_file ./texts_to_predict.txt

Notes
-----
- This framework uses BERTimbau as the core classifier.
- Interpretations are generated by a structured interpreter.
- An optional LLM rewriter can be enabled later, but it is disabled by default for reliability.
- The outputs are screening-oriented and non-diagnostic.
"""

