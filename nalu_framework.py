def _nr_flatten_metrics_for_csv(metrics: Dict[str, Any], model_name: str, split_name: str, condition: str = "tuned_thresholds") -> List[Dict[str, Any]]:
  """Flatten nested NALU metrics into paper-friendly CSV rows."""
  rows: List[Dict[str, Any]] = []
  if not metrics:
    return rows
  rows.append({"model": model_name, "split": split_name, "condition": condition, "head": "sentiment", "metric": "accuracy", "value": metrics.get("sentiment", {}).get("accuracy")})
  rows.append({"model": model_name, "split": split_name, "condition": condition, "head": "sentiment", "metric": "macro_f1", "value": metrics.get("sentiment", {}).get("macro_f1")})
  rows.append({"model": model_name, "split": split_name, "condition": condition, "head": "sentiment", "metric": "weighted_f1", "value": metrics.get("sentiment", {}).get("weighted_f1")})
  for head in ["emotion", "psychological", "behavioral", "risk", "clinical"]:
    h = metrics.get(head, {})
    for metric in ["macro_f1", "micro_f1", "hamming_loss", "avg_cardinality_true", "avg_cardinality_pred"]:
      rows.append({"model": model_name, "split": split_name, "condition": condition, "head": head, "metric": metric, "value": h.get(metric)})
    for label, vals in h.get("per_label", {}).items():
      rows.append({"model": model_name, "split": split_name, "condition": condition, "head": head, "metric": f"label_f1::{label}", "value": vals.get("f1")})
      rows.append({"model": model_name, "split": split_name, "condition": condition, "head": head, "metric": f"threshold::{label}", "value": vals.get("threshold")})
  for head, vals in metrics.get("regression", {}).items():
    for metric in ["mae", "rmse", "pearson"]:
      rows.append({"model": model_name, "split": split_name, "condition": condition, "head": head, "metric": metric, "value": vals.get(metric)})
  for head, vals in metrics.get("psychometric", {}).items():
    for metric in ["mae", "rmse", "pearson", "n_valid"]:
      rows.append({"model": model_name, "split": split_name, "condition": condition, "head": head, "metric": metric, "value": vals.get(metric)})
  rows.append({"model": model_name, "split": split_name, "condition": condition, "head": "overall", "metric": "composite_score", "value": metrics.get("composite_score")})
  return rows


def _nr_slice_arrays(d: Dict[str, np.ndarray], idx: np.ndarray) -> Dict[str, np.ndarray]:
  """Slice all numpy arrays in target/probability dictionaries."""
  out: Dict[str, np.ndarray] = {}
  for k, v in d.items():
    if isinstance(v, np.ndarray):
      out[k] = v[idx]
    else:
      out[k] = v
  return out


def _nr_primary_metric(metrics: Dict[str, Any], head: str, metric: str) -> Optional[float]:
  """Retrieve a metric from the nested metrics structure."""
  try:
    if head == "overall" and metric == "composite_score":
      return float(metrics.get("composite_score"))
    if head in ["sentiment", "emotion", "psychological", "behavioral", "risk", "clinical"]:
      return float(metrics[head][metric])
    if head in metrics.get("regression", {}):
      return float(metrics["regression"][head][metric])
    if head in metrics.get("psychometric", {}):
      return float(metrics["psychometric"][head][metric])
  except Exception:
    return None
  return None


def _nr_bootstrap_ci(
  trainer: "NALUTrainer",
  targets: Dict[str, np.ndarray],
  probs: Dict[str, np.ndarray],
  thresholds: Dict[str, Any],
  model_name: str,
  condition: str,
  n_bootstrap: int = 1000,
  seed: int = 42,
) -> pd.DataFrame:
  """Compute bootstrap confidence intervals for main paper metrics."""
  rng = np.random.default_rng(seed)
  n = len(targets["sentiment"])
  metric_specs = [
    ("sentiment", "accuracy"),
    ("sentiment", "macro_f1"),
    ("emotion", "macro_f1"),
    ("emotion", "micro_f1"),
    ("psychological", "macro_f1"),
    ("behavioral", "macro_f1"),
    ("risk", "macro_f1"),
    ("clinical", "macro_f1"),
    ("overall", "composite_score"),
  ]
  for col in trainer.schema.regression_cols:
    metric_specs.extend([(col, "mae"), (col, "rmse"), (col, "pearson")])
  for col in trainer.schema.psychometric_cols:
    metric_specs.extend([(col, "mae"), (col, "rmse"), (col, "pearson")])

  values: Dict[Tuple[str, str], List[float]] = {spec: [] for spec in metric_specs}
  for _ in tqdm(range(n_bootstrap), desc=f"Bootstrap CI {model_name}/{condition}", leave=False):
    idx = rng.integers(0, n, size=n)
    try:
      m = trainer.metrics_computer.compute(_nr_slice_arrays(targets, idx), _nr_slice_arrays(probs, idx), thresholds)
    except Exception:
      continue
    for spec in metric_specs:
      val = _nr_primary_metric(m, spec[0], spec[1])
      if val is not None and np.isfinite(val):
        values[spec].append(float(val))

  rows = []
  full_metrics = trainer.metrics_computer.compute(targets, probs, thresholds)
  for head, metric in metric_specs:
    arr = np.array(values[(head, metric)], dtype=float)
    point = _nr_primary_metric(full_metrics, head, metric)
    if arr.size == 0 or point is None:
      continue
    rows.append({
      "model": model_name,
      "condition": condition,
      "head": head,
      "metric": metric,
      "point_estimate": point,
      "bootstrap_mean": float(np.mean(arr)),
      "ci95_low": float(np.percentile(arr, 2.5)),
      "ci95_high": float(np.percentile(arr, 97.5)),
      "n_bootstrap": int(arr.size),
    })
  return pd.DataFrame(rows)


def _nr_paired_bootstrap_significance(
  trainer: "NALUTrainer",
  targets: Dict[str, np.ndarray],
  probs: Dict[str, np.ndarray],
  thresholds_a: Dict[str, Any],
  thresholds_b: Dict[str, Any],
  name_a: str,
  name_b: str,
  model_name: str,
  n_bootstrap: int = 1000,
  seed: int = 123,
) -> pd.DataFrame:
  """Paired bootstrap comparison between two evaluation conditions on the same test set."""
  rng = np.random.default_rng(seed)
  n = len(targets["sentiment"])
  metric_specs = [
    ("emotion", "macro_f1", "higher"),
    ("psychological", "macro_f1", "higher"),
    ("behavioral", "macro_f1", "higher"),
    ("risk", "macro_f1", "higher"),
    ("clinical", "macro_f1", "higher"),
    ("overall", "composite_score", "higher"),
  ]
  diffs: Dict[Tuple[str, str], List[float]] = {(h, m): [] for h, m, _ in metric_specs}
  for _ in tqdm(range(n_bootstrap), desc=f"Paired bootstrap {name_a} vs {name_b}", leave=False):
    idx = rng.integers(0, n, size=n)
    ts = _nr_slice_arrays(targets, idx)
    ps = _nr_slice_arrays(probs, idx)
    try:
      ma = trainer.metrics_computer.compute(ts, ps, thresholds_a)
      mb = trainer.metrics_computer.compute(ts, ps, thresholds_b)
    except Exception:
      continue
    for h, m, _direction in metric_specs:
      va = _nr_primary_metric(ma, h, m)
      vb = _nr_primary_metric(mb, h, m)
      if va is not None and vb is not None:
        diffs[(h, m)].append(float(va - vb))
  rows = []
  ma_full = trainer.metrics_computer.compute(targets, probs, thresholds_a)
  mb_full = trainer.metrics_computer.compute(targets, probs, thresholds_b)
  for h, m, direction in metric_specs:
    arr = np.array(diffs[(h, m)], dtype=float)
    if arr.size == 0:
      continue
    observed = (_nr_primary_metric(ma_full, h, m) or 0.0) - (_nr_primary_metric(mb_full, h, m) or 0.0)
    # Two-sided paired bootstrap p-value around zero.
    p_two = 2.0 * min(float(np.mean(arr <= 0.0)), float(np.mean(arr >= 0.0)))
    p_two = min(1.0, max(0.0, p_two))
    rows.append({
      "model": model_name,
      "condition_a": name_a,
      "condition_b": name_b,
      "head": h,
      "metric": m,
      "direction": direction,
      "observed_difference_a_minus_b": float(observed),
      "diff_ci95_low": float(np.percentile(arr, 2.5)),
      "diff_ci95_high": float(np.percentile(arr, 97.5)),
      "paired_bootstrap_p_two_sided": p_two,
      "n_bootstrap": int(arr.size),
    })
  return pd.DataFrame(rows)


def _nr_save_prediction_npz(path: Union[str, Path], targets: Dict[str, np.ndarray], probs: Dict[str, np.ndarray]) -> None:
  """Save targets and probabilities into an npz archive for reproducible post-hoc analysis."""
  arrays: Dict[str, np.ndarray] = {}
  for prefix, d in [("target", targets), ("prob", probs)]:
    for k, v in d.items():
      if isinstance(v, np.ndarray):
        arrays[f"{prefix}_{k}"] = v
  np.savez_compressed(path, **arrays)


def _nr_plot_bar_comparison(csv_path: Union[str, Path], out_path: Union[str, Path]) -> None:
  """Create a compact bar chart comparing tuned vs fixed thresholds for main heads."""
  if plt is None:
    return
  df = pd.read_csv(csv_path)
  metric = "macro_f1"
  heads = ["emotion", "psychological", "behavioral", "risk", "clinical"]
  sub = df[(df["metric"] == metric) & (df["head"].isin(heads))]
  if sub.empty:
    return
  pivot = sub.pivot_table(index="head", columns="condition", values="value", aggfunc="first").reindex(heads)
  ax = pivot.plot(kind="bar", figsize=(9, 5))
  ax.set_ylabel("Macro-F1")
  ax.set_xlabel("Prediction head")
  ax.set_title("Tuned thresholds vs fixed 0.5 threshold")
  ax.legend(title="Condition")
  plt.tight_layout()
  plt.savefig(out_path, dpi=300, bbox_inches="tight")
  plt.close()


def _nr_plot_thresholds(per_label_csv: Union[str, Path], out_path: Union[str, Path]) -> None:
  """Plot learned thresholds per label for the tuned model."""
  if plt is None:
    return
  df = pd.read_csv(per_label_csv)
  df = df[(df["split"] == "test") & df["threshold"].notna()]
  if df.empty:
    return
  df = df.sort_values(["head", "label"])
  labels = [f"{h}:{str(l).replace('emotion_', '')}" for h, l in zip(df["head"], df["label"])]
  fig_h = max(5, 0.25 * len(labels))
  fig, ax = plt.subplots(figsize=(9, fig_h))
  ax.barh(labels, df["threshold"].astype(float).values)
  ax.set_xlabel("Optimized threshold")
  ax.set_title("Per-label optimized thresholds")
  plt.tight_layout()
  plt.savefig(out_path, dpi=300, bbox_inches="tight")
  plt.close()


def _nr_plot_bootstrap_ci(ci_csv: Union[str, Path], out_path: Union[str, Path]) -> None:
  """Plot bootstrap CIs for key heads."""
  if plt is None:
    return
  df = pd.read_csv(ci_csv)
  df = df[(df["condition"] == "tuned_thresholds") & (df["metric"].isin(["macro_f1", "composite_score"]))]
  df = df[df["head"].isin(["sentiment", "emotion", "psychological", "behavioral", "risk", "clinical", "overall"])]
  if df.empty:
    return
  df = df.copy()
  df["label"] = df["head"] + " / " + df["metric"]
  y = np.arange(len(df))
  x = df["point_estimate"].astype(float).values
  xerr = np.vstack([x - df["ci95_low"].astype(float).values, df["ci95_high"].astype(float).values - x])
  fig, ax = plt.subplots(figsize=(9, max(4, 0.45 * len(df))))
  ax.errorbar(x, y, xerr=xerr, fmt="o", capsize=4)
  ax.set_yticks(y)
  ax.set_yticklabels(df["label"].tolist())
  ax.set_xlabel("Metric value with 95% bootstrap CI")
  ax.set_title("Bootstrap confidence intervals")
  plt.tight_layout()
  plt.savefig(out_path, dpi=300, bbox_inches="tight")
  plt.close()


def generate_newresults_package(
  trainer: "NALUTrainer",
  val_targets: Dict[str, np.ndarray],
  val_probs: Dict[str, np.ndarray],
  test_targets: Dict[str, np.ndarray],
  test_probs: Dict[str, np.ndarray],
  tuned_thresholds: Dict[str, Any],
  tuned_val_metrics: Dict[str, Any],
  tuned_test_metrics: Dict[str, Any],
  n_bootstrap: int = 1000,
  model_name: str = "NALU-full",
) -> Dict[str, Any]:
  """Generate all additional statistical tests, ablations and paper-ready outputs for a trained model."""
  out_dir = ensure_dir(trainer.reports_dir / "new_results")
  figs_dir = ensure_dir(out_dir / "figures")
  fixed_thresholds = trainer.metrics_computer._default_thresholds(trainer.schema)

  fixed_val_metrics = trainer.metrics_computer.compute(val_targets, val_probs, fixed_thresholds)
  fixed_test_metrics = trainer.metrics_computer.compute(test_targets, test_probs, fixed_thresholds)

  json_dump(fixed_val_metrics, out_dir / "val_metrics_fixed_threshold_0p5.json")
  json_dump(fixed_test_metrics, out_dir / "test_metrics_fixed_threshold_0p5.json")
  json_dump(tuned_val_metrics, out_dir / "val_metrics_tuned_thresholds.json")
  json_dump(tuned_test_metrics, out_dir / "test_metrics_tuned_thresholds.json")
  _nr_save_prediction_npz(out_dir / "val_targets_probs.npz", val_targets, val_probs)
  _nr_save_prediction_npz(out_dir / "test_targets_probs.npz", test_targets, test_probs)

  rows = []
  rows += _nr_flatten_metrics_for_csv(tuned_val_metrics, model_name, "validation", "tuned_thresholds")
  rows += _nr_flatten_metrics_for_csv(fixed_val_metrics, model_name, "validation", "fixed_threshold_0p5")
  rows += _nr_flatten_metrics_for_csv(tuned_test_metrics, model_name, "test", "tuned_thresholds")
  rows += _nr_flatten_metrics_for_csv(fixed_test_metrics, model_name, "test", "fixed_threshold_0p5")
  metrics_long = pd.DataFrame(rows)
  metrics_long_csv = out_dir / "metrics_long_tuned_vs_fixed.csv"
  metrics_long.to_csv(metrics_long_csv, index=False, encoding="utf-8")

  main_heads = ["sentiment", "emotion", "psychological", "behavioral", "risk", "clinical", "overall"]
  main_metrics = ["accuracy", "macro_f1", "micro_f1", "composite_score"]
  paper_summary = metrics_long[(metrics_long["split"] == "test") & (metrics_long["head"].isin(main_heads)) & (metrics_long["metric"].isin(main_metrics))]
  paper_summary.to_csv(out_dir / "table_test_tuned_vs_fixed_thresholds.csv", index=False, encoding="utf-8")

  # Bootstrap confidence intervals and paired significance.
  ci_tuned = _nr_bootstrap_ci(trainer, test_targets, test_probs, tuned_thresholds, model_name, "tuned_thresholds", n_bootstrap=n_bootstrap, seed=trainer.config.seed)
  ci_fixed = _nr_bootstrap_ci(trainer, test_targets, test_probs, fixed_thresholds, model_name, "fixed_threshold_0p5", n_bootstrap=n_bootstrap, seed=trainer.config.seed + 1)
  ci_df = pd.concat([ci_tuned, ci_fixed], ignore_index=True)
  ci_csv = out_dir / "bootstrap_confidence_intervals.csv"
  ci_df.to_csv(ci_csv, index=False, encoding="utf-8")

  sig_df = _nr_paired_bootstrap_significance(trainer, test_targets, test_probs, tuned_thresholds, fixed_thresholds, "tuned_thresholds", "fixed_threshold_0p5", model_name, n_bootstrap=n_bootstrap, seed=trainer.config.seed + 2)
  sig_csv = out_dir / "paired_bootstrap_tuned_vs_fixed.csv"
  sig_df.to_csv(sig_csv, index=False, encoding="utf-8")

  # Export per-label paper table for tuned/fixed test conditions.
  per_label_rows = []
  for condition, metrics in [("tuned_thresholds", tuned_test_metrics), ("fixed_threshold_0p5", fixed_test_metrics)]:
    for head in ["emotion", "psychological", "behavioral", "risk", "clinical"]:
      for label, vals in metrics[head].get("per_label", {}).items():
        per_label_rows.append({
          "condition": condition,
          "head": head,
          "label": label,
          "threshold": vals.get("threshold"),
          "f1": vals.get("f1"),
          "positive_true": vals.get("positive_true"),
          "positive_pred": vals.get("positive_pred"),
        })
  per_label_csv = out_dir / "table_per_label_tuned_vs_fixed.csv"
  pd.DataFrame(per_label_rows).to_csv(per_label_csv, index=False, encoding="utf-8")

  # Plots for manuscript/supplementary material.
  _nr_plot_bar_comparison(metrics_long_csv, figs_dir / "fig_tuned_vs_fixed_macro_f1.png")
  _nr_plot_thresholds(per_label_csv, figs_dir / "fig_optimized_thresholds.png")
  _nr_plot_bootstrap_ci(ci_csv, figs_dir / "fig_bootstrap_ci.png")

  readme = out_dir / "README_new_results.txt"
  readme.write_text(
    "NALU new-results package\n"
    "=============================\n\n"
    "This folder contains paper-ready outputs generated after training.\n"
    "Main files:\n"
    "- table_test_tuned_vs_fixed_thresholds.csv: main threshold ablation table.\n"
    "- bootstrap_confidence_intervals.csv: 95% bootstrap confidence intervals.\n"
    "- paired_bootstrap_tuned_vs_fixed.csv: paired bootstrap significance tests.\n"
    "- table_per_label_tuned_vs_fixed.csv: per-label threshold/F1 table.\n"
    "- figures/*.png: manuscript/supplementary figures.\n"
    "- test_targets_probs.npz: saved targets and probabilities for reproducibility.\n\n"
    "Interpretation: tuned thresholds are compared against a fixed 0.5 threshold without retraining.\n",
    encoding="utf-8",
  )

  return {
    "new_results_dir": str(out_dir),
    "metrics_long_csv": str(metrics_long_csv),
    "threshold_ablation_csv": str(out_dir / "table_test_tuned_vs_fixed_thresholds.csv"),
    "bootstrap_ci_csv": str(ci_csv),
    "paired_bootstrap_csv": str(sig_csv),
    "per_label_ablation_csv": str(per_label_csv),
    "fig_tuned_vs_fixed": str(figs_dir / "fig_tuned_vs_fixed_macro_f1.png"),
    "fig_thresholds": str(figs_dir / "fig_optimized_thresholds.png"),
    "fig_bootstrap_ci": str(figs_dir / "fig_bootstrap_ci.png"),
    "fixed_threshold_test_metrics_json": str(out_dir / "test_metrics_fixed_threshold_0p5.json"),
    "test_targets_probs_npz": str(out_dir / "test_targets_probs.npz"),
  }


def compare_trained_runs(run_dirs: List[Union[str, Path]], output_dir: Union[str, Path]) -> Dict[str, str]:
  """Compare completed training runs using their saved test metrics."""
  out_dir = ensure_dir(output_dir)
  rows: List[Dict[str, Any]] = []
  for rd in run_dirs:
    rd = Path(rd)
    metrics_path = rd / "reports" / "test_metrics.json"
    summary_path = rd / "run_summary.json"
    if not metrics_path.exists():
      continue
    metrics = json_load(metrics_path)
    model_name = rd.name
    if summary_path.exists():
      try:
        summary = json_load(summary_path)
        model_name = summary.get("model_name", model_name)
      except Exception:
        pass
    rows.extend(_nr_flatten_metrics_for_csv(metrics, model_name, "test", "tuned_thresholds"))
    fixed_path = rd / "reports" / "new_results" / "test_metrics_fixed_threshold_0p5.json"
    if fixed_path.exists():
      rows.extend(_nr_flatten_metrics_for_csv(json_load(fixed_path), model_name, "test", "fixed_threshold_0p5"))
  df = pd.DataFrame(rows)
  long_csv = out_dir / "all_runs_metrics_long.csv"
  df.to_csv(long_csv, index=False, encoding="utf-8")
  main = df[(df["head"].isin(["sentiment", "emotion", "psychological", "behavioral", "risk", "clinical", "overall"])) & (df["metric"].isin(["accuracy", "macro_f1", "micro_f1", "composite_score"]))]
  main_csv = out_dir / "table_model_ablation_comparison.csv"
  main.to_csv(main_csv, index=False, encoding="utf-8")
  if plt is not None and not main.empty:
    plot_df = main[(main["condition"] == "tuned_thresholds") & (main["metric"].isin(["macro_f1", "composite_score"]))]
    pivot = plot_df.pivot_table(index="model", columns="head", values="value", aggfunc="first")
    ax = pivot.plot(kind="bar", figsize=(11, 5))
    ax.set_ylabel("Metric value")
    ax.set_title("Model ablation comparison")
    plt.tight_layout()
    fig_path = out_dir / "fig_model_ablation_comparison.png"
    plt.savefig(fig_path, dpi=300, bbox_inches="tight")
    plt.close()
  else:
    fig_path = out_dir / "fig_model_ablation_comparison.png"
  return {"all_runs_metrics_long_csv": str(long_csv), "model_ablation_table_csv": str(main_csv), "model_ablation_figure": str(fig_path)}


def _suite_make_single_task_lambdas(head: str) -> Dict[str, str]:
  """Return CLI lambda overrides for a single-task baseline using the same architecture but one active loss."""
  lambdas = {
    "--lambda_sentiment": "0.0",
    "--lambda_emotion": "0.0",
    "--lambda_psychological": "0.0",
    "--lambda_behavioral": "0.0",
    "--lambda_risk": "0.0",
    "--lambda_clinical": "0.0",
    "--lambda_regression": "0.0",
    "--lambda_psychometric": "0.0",
  }
  mapping = {
    "sentiment": "--lambda_sentiment",
    "emotion": "--lambda_emotion",
    "psychological": "--lambda_psychological",
    "behavioral": "--lambda_behavioral",
    "risk": "--lambda_risk",
    "clinical": "--lambda_clinical",
    "regression": "--lambda_regression",
    "psychometric": "--lambda_psychometric",
  }
  if head not in mapping:
    raise ValueError(f"Unknown single-task head: {head}")
  lambdas[mapping[head]] = "1.0"
  return lambdas


def run_newresults_training_suite(args: argparse.Namespace) -> Dict[str, Any]:
  """Run full model + requested ablations from scratch and produce model-comparison tables."""
  root = ensure_dir(args.output_root)
  script_path = Path(__file__).resolve()

  common = [
    sys.executable, str(script_path), "train",
    "--dataset", args.dataset,
    "--pretrained_model_name", args.pretrained_model_name,
    "--epochs", str(args.epochs),
    "--batch_size", str(args.batch_size),
    "--eval_batch_size", str(args.eval_batch_size),
    "--max_length", str(args.max_length),
    "--learning_rate", str(args.learning_rate),
    "--head_learning_rate", str(args.head_learning_rate),
    "--seed", str(args.seed),
    "--bootstrap_iterations", str(args.bootstrap_iterations),
  ]
  if args.gradient_checkpointing:
    common.append("--gradient_checkpointing")

  runs: List[Tuple[str, List[str]]] = []
  runs.append(("01_full_nalu", []))
  runs.append(("02_bce_loss_ablation", ["--disable_asymmetric_focal_loss"]))
  runs.append(("03_no_psychometric_loss_ablation", ["--lambda_psychometric", "0.0"]))

  for head in args.single_task_heads:
    overrides = []
    for k, v in _suite_make_single_task_lambdas(head).items():
      overrides.extend([k, v])
    runs.append((f"single_task_{head}", overrides))

  # Optional real-only run if the dataset has a source column and source values are provided.
  if args.real_only_column and args.real_only_values:
    df = pd.read_csv(args.dataset)
    if args.real_only_column not in df.columns:
      raise ValueError(f"real_only_column '{args.real_only_column}' was not found in dataset.")
    vals = [x.strip() for x in args.real_only_values.split(",") if x.strip()]
    real_df = df[df[args.real_only_column].astype(str).isin(vals)].copy()
    if len(real_df) == 0:
      raise ValueError("The real-only filter produced zero rows. Check --real_only_values.")
    real_csv = root / "real_only_filtered_dataset.csv"
    real_df.to_csv(real_csv, index=False, encoding="utf-8")
    real_common = list(common)
    real_common[real_common.index(args.dataset)] = str(real_csv) if args.dataset in real_common else str(real_csv)
    # Easier: override dataset path in command construction below with special marker.
    runs.append(("04_real_only_data_ablation", ["__DATASET_OVERRIDE__", str(real_csv)]))

  completed_dirs: List[str] = []
  manifest_rows: List[Dict[str, Any]] = []
  for run_name, extra in runs:
    run_dir = root / run_name
    cmd = list(common)
    if "__DATASET_OVERRIDE__" in extra:
      idx = extra.index("__DATASET_OVERRIDE__")
      dataset_override = extra[idx + 1]
      extra = extra[:idx] + extra[idx + 2:]
      # Replace dataset argument value.
      ds_i = cmd.index("--dataset") + 1
      cmd[ds_i] = dataset_override
    cmd.extend(["--output_dir", str(run_dir)])
    cmd.extend(extra)
    manifest_rows.append({"run_name": run_name, "run_dir": str(run_dir), "command": " ".join(cmd)})
    print("\n" + "=" * 120)
    print(f"Running new-results experiment: {run_name}")
    print(" ".join(cmd))
    print("=" * 120)
    subprocess.run(cmd, check=True)
    completed_dirs.append(str(run_dir))

  manifest_csv = root / "newresults_suite_manifest.csv"
  pd.DataFrame(manifest_rows).to_csv(manifest_csv, index=False, encoding="utf-8")
  comparison = compare_trained_runs(completed_dirs, root / "model_comparison")
  summary = {"suite_root": str(root), "manifest_csv": str(manifest_csv), "completed_runs": completed_dirs, "comparison": comparison}
  json_dump(summary, root / "newresults_suite_summary.json")
  return summary



