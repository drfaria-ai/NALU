  def train_and_evaluate(self, data_module: NALUDataModule) -> Dict[str, Any]:
    """Full train/val/test pipeline."""
    df = data_module.load()
    train_df, val_df, test_df = data_module.split()

    description = data_module.describe()
    json_dump(description, self.reports_dir / "dataset_description.json")

    if self.config.resume_from_checkpoint:
      current_config = self.config
      resume_path = self.config.resume_from_checkpoint
      self.load_checkpoint(resume_path)
      # Preserve the current run configuration after loading weights/tokenizer from the previous checkpoint.
      self.config = current_config
      print(f"Resumed weights from: {resume_path}")

    train_loader, val_loader, test_loader = self._build_loaders(train_df, val_df, test_df)
    class_weights = self._compute_class_weights(train_df)

    criterion = MultiHeadLoss(
      config=self.config,
      sentiment_class_weights=class_weights["sentiment"],
      emotion_pos_weight=class_weights["emotion"],
      psychological_pos_weight=class_weights["psychological"],
      behavioral_pos_weight=class_weights["behavioral"],
      risk_pos_weight=class_weights["risk"],
      clinical_pos_weight=class_weights["clinical"],
    )

    total_training_steps = math.ceil(len(train_loader) / self.config.gradient_accumulation_steps) * self.config.epochs
    optimizer, scheduler = self._build_optimizer_scheduler(total_training_steps)

    best_score = -1.0
    best_epoch = -1
    no_improve = 0
    history = []

    for epoch in range(self.config.epochs):
      train_loss = self._run_epoch(
        loader=train_loader,
        criterion=criterion,
        optimizer=optimizer,
        scheduler=scheduler,
        train=True,
        epoch_idx=epoch,
      )

      val_loss = self._run_epoch(
        loader=val_loader,
        criterion=criterion,
        optimizer=None,
        scheduler=None,
        train=False,
        epoch_idx=epoch,
      )

      val_targets, val_probs, _ = self._collect_predictions(val_loader)
      thresholds = self.metrics_computer.tune_thresholds(val_targets, val_probs)
      val_metrics = self.metrics_computer.compute(val_targets, val_probs, thresholds)

      epoch_report = {
        "epoch": epoch + 1,
        "train_loss": train_loss,
        "val_loss": val_loss,
        "val_metrics": val_metrics,
        "thresholds": thresholds,
      }
      history.append(epoch_report)
      json_dump({"history": history}, self.reports_dir / "training_history.json")

      current_score = val_metrics.get(self.config.save_best_metric, val_metrics["composite_score"])
      print(f"\nEpoch {epoch+1}/{self.config.epochs}")
      print(f"Validation composite score: {current_score:.4f}")
      print(f"Validation sentiment macro-F1: {val_metrics['sentiment']['macro_f1']:.4f}")
      print(f"Validation emotion macro-F1: {val_metrics['emotion']['macro_f1']:.4f}")
      print(f"Validation psychological macro-F1: {val_metrics['psychological']['macro_f1']:.4f}")
      print(f"Validation behavioral macro-F1: {val_metrics['behavioral']['macro_f1']:.4f}")
      print(f"Validation risk macro-F1: {val_metrics['risk']['macro_f1']:.4f}")
      print(f"Validation clinical macro-F1: {val_metrics['clinical']['macro_f1']:.4f}")

      if current_score > best_score:
        best_score = current_score
        best_epoch = epoch + 1
        no_improve = 0
        self.best_metrics = val_metrics
        self.best_thresholds = thresholds
        self._save_checkpoint("best_model", thresholds=thresholds, metrics=val_metrics)
      else:
        no_improve += 1
        if no_improve >= self.config.early_stopping_patience:
          print(f"Early stopping triggered at epoch {epoch+1}.")
          break

    # Load best checkpoint before final validation/test evaluation.
    self.load_checkpoint(self.checkpoint_dir / "best_model")

    # Re-collect validation predictions from the best checkpoint. These are used for
    # final reporting, threshold ablations and statistical analysis.
    val_targets_final, val_probs_final, _ = self._collect_predictions(val_loader)
    final_val_metrics = self.metrics_computer.compute(val_targets_final, val_probs_final, self.best_thresholds or {})
    json_dump(final_val_metrics, self.reports_dir / "validation_metrics_best_checkpoint.json")

    # Evaluate on test.
    test_targets, test_probs, test_meta = self._collect_predictions(test_loader)
    test_metrics = self.metrics_computer.compute(test_targets, test_probs, self.best_thresholds or {})
    json_dump(test_metrics, self.reports_dir / "test_metrics.json")

    # Build prediction outputs and structured interpretations for the test split.
    test_predictions_df = self._build_predictions_dataframe(
      metadata_df=test_meta,
      probs=test_probs,
      thresholds=self.best_thresholds or {},
    )
    test_predictions_path = self.predictions_dir / "test_predictions_with_interpretation.csv"
    test_predictions_df.to_csv(test_predictions_path, index=False, encoding="utf-8")

    # Save readable samples for quick inspection.
    sample_preview = test_predictions_df.head(30)
    sample_preview.to_csv(self.predictions_dir / "test_predictions_preview.csv", index=False, encoding="utf-8")

    # Paper-ready validation/reporting package saved before the final console examples.
    paper_ready_report = self._make_paper_ready_report(
      validation_metrics=final_val_metrics or self.best_metrics or {},
      test_metrics=test_metrics,
      thresholds=self.best_thresholds or {},
      prediction_df=test_predictions_df,
    )

    # New-results package: fixed-threshold ablation, bootstrap confidence intervals,
    # paired significance tests and manuscript figures.
    bootstrap_iterations = int(getattr(self.config, "bootstrap_iterations", 1000))
    model_name = str(getattr(self.config, "model_name", Path(self.config.output_dir).name))
    new_results_report = generate_newresults_package(
      trainer=self,
      val_targets=val_targets_final,
      val_probs=val_probs_final,
      test_targets=test_targets,
      test_probs=test_probs,
      tuned_thresholds=self.best_thresholds or {},
      tuned_val_metrics=final_val_metrics,
      tuned_test_metrics=test_metrics,
      n_bootstrap=bootstrap_iterations,
      model_name=model_name,
    )

    summary = {
      "best_epoch": best_epoch,
      "best_validation_metrics": self.best_metrics,
      "test_metrics": test_metrics,
      "thresholds": self.best_thresholds,
      "device": str(self.device),
      "dataset_path": self.config.dataset_path,
      "test_predictions_csv": str(test_predictions_path),
      "paper_ready_report": paper_ready_report,
      "new_results_report": new_results_report,
      "interpretation_calibration": self._interpretation_calibration_config(),
    }
    json_dump(summary, self.output_dir / "run_summary.json")

    print("\nTraining finished.")
    print(f"Best epoch: {best_epoch}")
    print(f"Device used: {self.device}")
    print(f"Best model saved at: {self.checkpoint_dir / 'best_model'}")
    print(f"Test predictions with interpretations saved at: {test_predictions_path}")
    return summary

