  @torch.no_grad()
  def predict_texts(
    self,
    texts: Union[str, List[str]],
    use_llm_rewriter: Optional[bool] = None,
  ) -> List[Dict[str, Any]]:
    """
    Predict all heads and return structured + optional natural-language interpretations.

    This is the function intended for future multimodal integration:
    it can receive one text or a list/vector of text chunks.
    """
    if isinstance(texts, str):
      texts = [texts]

    if self.best_thresholds is None:
      self.best_thresholds = self.metrics_computer._default_thresholds(self.schema)

    self.model.eval()

    encoded = self.tokenizer(
      list(texts),
      truncation=True,
      padding=True,
      max_length=self.config.max_length,
      return_tensors="pt",
    )

    encoded = {k: v.to(self.device) for k, v in encoded.items()}
    outputs = self.model(input_ids=encoded["input_ids"], attention_mask=encoded["attention_mask"])

    sentiment_probs = torch.softmax(outputs["sentiment_logits"], dim=-1).cpu().numpy()
    emotion_probs = torch.sigmoid(outputs["emotion_logits"]).cpu().numpy()
    psychological_probs = torch.sigmoid(outputs["psychological_logits"]).cpu().numpy()
    behavioral_probs = torch.sigmoid(outputs["behavioral_logits"]).cpu().numpy()
    risk_probs = torch.sigmoid(outputs["risk_logits"]).cpu().numpy()
    clinical_probs = torch.sigmoid(outputs["clinical_logits"]).cpu().numpy()
    regression_vals = outputs["regression"].cpu().numpy()
    psychometric_vals = outputs["psychometric"].cpu().numpy()

    use_llm = self.config.use_llm_rewriter if use_llm_rewriter is None else use_llm_rewriter

    results = []
    for i, text in enumerate(texts):
      result = {
        "text": text,
        "sentiment_probs": dict(zip(
          [c.replace("sentiment_", "") for c in self.schema.sentiment_cols],
          sentiment_probs[i].tolist(),
        )),
        "emotion_probs": dict(zip(
          [c.replace("emotion_", "") for c in self.schema.emotion_cols],
          emotion_probs[i].tolist(),
        )),
        "psychological_probs": dict(zip(self.schema.psychological_cols, psychological_probs[i].tolist())),
        "behavioral_probs": dict(zip(self.schema.behavioral_cols, behavioral_probs[i].tolist())),
        "risk_probs": dict(zip(self.schema.risk_cols, risk_probs[i].tolist())),
        "clinical_probs": dict(zip(self.schema.clinical_cols, clinical_probs[i].tolist())),
        "regression": {
          "valence_russell": float(regression_vals[i][0]),
          "arousal_russell": float(regression_vals[i][1]),
        },
        "psychometric": dict(zip(self.schema.psychometric_cols, psychometric_vals[i].tolist())),
        "thresholds_used": self.best_thresholds,
      }

      structured = self.interpreter.interpret(result)
      result["structured_interpretation"] = structured

      if use_llm:
        result["llm_final_comment_pt"] = self.llm_rewriter.rewrite(structured)
      else:
        result["llm_final_comment_pt"] = structured["short_summary_pt"]

      results.append(result)

    return results

  def _build_predictions_dataframe(
    self,
    metadata_df: pd.DataFrame,
    probs: Dict[str, np.ndarray],
    thresholds: Dict[str, float],
  ) -> pd.DataFrame:
    """Build a rich dataframe with probabilities, binary outputs, and interpretations."""
    rows = []

    for i in range(len(metadata_df)):
      sentiment_map = dict(zip([c.replace("sentiment_", "") for c in self.schema.sentiment_cols], probs["sentiment"][i].tolist()))
      emotion_map = dict(zip([c.replace("emotion_", "") for c in self.schema.emotion_cols], probs["emotion"][i].tolist()))
      psychological_map = dict(zip(self.schema.psychological_cols, probs["psychological"][i].tolist()))
      behavioral_map = dict(zip(self.schema.behavioral_cols, probs["behavioral"][i].tolist()))
      risk_map = dict(zip(self.schema.risk_cols, probs["risk"][i].tolist()))
      clinical_map = dict(zip(self.schema.clinical_cols, probs["clinical"][i].tolist()))
      regression_map = {
        "valence_russell": float(probs["regression"][i][0]),
        "arousal_russell": float(probs["regression"][i][1]),
      }
      psychometric_map = dict(zip(self.schema.psychometric_cols, probs.get("psychometric", np.zeros((len(metadata_df), len(self.schema.psychometric_cols))))[i].tolist()))

      pred_bundle = {
        "text": metadata_df.iloc[i]["text"],
        "sentiment_probs": sentiment_map,
        "emotion_probs": emotion_map,
        "psychological_probs": psychological_map,
        "behavioral_probs": behavioral_map,
        "risk_probs": risk_map,
        "clinical_probs": clinical_map,
        "regression": regression_map,
        "psychometric": psychometric_map,
      }

      interpretation = self.interpreter.interpret(pred_bundle)

      row = {
        "sample_id": metadata_df.iloc[i]["sample_id"],
        "text": metadata_df.iloc[i]["text"],
        "predicted_sentiment": max(sentiment_map, key=sentiment_map.get),
        "predicted_sentiment_score": max(sentiment_map.values()),
        "valence_russell_pred": regression_map["valence_russell"],
        "arousal_russell_pred": regression_map["arousal_russell"],
        "phq9_score_pred_norm": psychometric_map.get("phq9_score_norm", 0.0),
        "gad7_score_pred_norm": psychometric_map.get("gad7_score_norm", 0.0),
        "dass_depression_pred_norm": psychometric_map.get("dass_depression_norm", 0.0),
        "dass_anxiety_pred_norm": psychometric_map.get("dass_anxiety_norm", 0.0),
        "dass_stress_pred_norm": psychometric_map.get("dass_stress_norm", 0.0),
        "psychometric_summary_pt": interpretation.get("psychometric_summary_pt", ""),
        "alert_level": interpretation["alert"]["level"],
        "alert_message_pt": interpretation["alert"]["message"],
        "short_summary_pt": interpretation["short_summary_pt"],
        "clinical_interpretation_pt": interpretation["clinical_interpretation_pt"],
        "suggested_focus_pt": " | ".join(interpretation["suggested_focus_pt"]),
        "non_diagnostic_notice_pt": interpretation["non_diagnostic_notice_pt"],
        "active_clinical_labels": " | ".join([x["label"] for x in interpretation.get("active_clinical", [])]),
      }

      # Add probabilities for each head.
      for k, v in sentiment_map.items():
        row[f"prob_sentiment_{k}"] = v
      for k, v in emotion_map.items():
        row[f"prob_emotion_{k}"] = v
        row[f"pred_emotion_{k}"] = int(v >= self.metrics_computer.get_threshold_for_label(thresholds, "emotion", f"emotion_{k}", 0.5))
      for k, v in psychological_map.items():
        row[f"prob_{k}"] = v
        row[f"pred_{k}"] = int(v >= self.metrics_computer.get_threshold_for_label(thresholds, "psychological", k, 0.5))
      for k, v in behavioral_map.items():
        row[f"prob_{k}"] = v
        row[f"pred_{k}"] = int(v >= self.metrics_computer.get_threshold_for_label(thresholds, "behavioral", k, 0.5))
      for k, v in risk_map.items():
        row[f"prob_{k}"] = v
        row[f"pred_{k}"] = int(v >= self.metrics_computer.get_threshold_for_label(thresholds, "risk", k, 0.5))
      for k, v in clinical_map.items():
        row[f"prob_{k}"] = v
        row[f"pred_{k}"] = int(v >= self.metrics_computer.get_threshold_for_label(thresholds, "clinical", k, 0.5))

      rows.append(row)

    return pd.DataFrame(rows)



