class OptionalLLMRewriter:
  """
  Optional final redaction layer.

  This is intentionally optional and comes after the structured interpreter.
  The structured output remains the source of truth.
  """

  def __init__(self, model_name: str, max_new_tokens: int = 180):
    self.model_name = model_name.strip()
    self.max_new_tokens = max_new_tokens
    self.pipe = None

    if self.model_name and hf_pipeline is not None:
      try:
        self.pipe = hf_pipeline(
          "text-generation",
          model=self.model_name,
          max_new_tokens=self.max_new_tokens,
        )
      except Exception:
        self.pipe = None

  def rewrite(self, structured_output: Dict[str, Any]) -> str:
    """Produce a more natural Portuguese paragraph if an LLM is available."""
    if self.pipe is None:
      return structured_output["short_summary_pt"]

    prompt = textwrap.dedent(f"""
    Você é um assistente clínico de apoio, não diagnóstico.
    Reescreva em português claro e breve o resumo abaixo para um psicólogo.
    Não invente fatos. Não use linguagem de diagnóstico definitivo. Máximo 4 frases.

    Resumo:
    {json.dumps(structured_output, ensure_ascii=False, indent=2)}
    """).strip()

    try:
      out = self.pipe(prompt, do_sample=False)[0]["generated_text"]
      # Return only the generated tail when possible.
      if out.startswith(prompt):
        out = out[len(prompt):].strip()
      return out.strip()
    except Exception:
      return structured_output["short_summary_pt"]


