  # ---------------------------
  # Weight helpers
  # ---------------------------

  def _compute_class_weights(self, train_df: pd.DataFrame) -> Dict[str, torch.Tensor]:
    """Compute class weights for sentiment and pos_weight for multi-label heads."""
    eps = 1e-6

    # Sentiment CE weights.
    sentiment_counts = train_df[self.schema.sentiment_cols].sum(axis=0).values.astype(float)
    sentiment_weights = sentiment_counts.sum() / (len(sentiment_counts) * (sentiment_counts + eps))
    sentiment_weights = torch.tensor(sentiment_weights, dtype=torch.float32, device=self.device)

    def pos_weight(cols: List[str]) -> torch.Tensor:
      arr = train_df[cols].values.astype(float)
      pos = arr.sum(axis=0)
      neg = len(arr) - pos
      pw = (neg + eps) / (pos + eps)
      pw = np.clip(pw, 1.0, 50.0)
      return torch.tensor(pw, dtype=torch.float32, device=self.device)

    return {
      "sentiment": sentiment_weights,
      "emotion": pos_weight(self.schema.emotion_cols),
      "psychological": pos_weight(self.schema.psychological_cols),
      "behavioral": pos_weight(self.schema.behavioral_cols),
      "risk": pos_weight(self.schema.risk_cols),
      "clinical": pos_weight(self.schema.clinical_cols),
    }

