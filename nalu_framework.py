    print(json.dumps(summary, ensure_ascii=False, indent=2))

    # Show a few interpreted examples from the test prediction file.
    pred_file = Path(summary["test_predictions_csv"])
    if pred_file.exists():
      preview_df = pd.read_csv(pred_file).head(5)
      print("\nExemplos do test set com interpretação final:")
