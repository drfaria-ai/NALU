def safe_float(x: Any, default: float = 0.0) -> float:
  """Convert to float safely."""
  try:
    return float(x)
  except Exception:
    return default


def sigmoid_np(x: np.ndarray) -> np.ndarray:
  """Numerically stable sigmoid in numpy."""
  return 1.0 / (1.0 + np.exp(-x))


def softmax_np(x: np.ndarray, axis: int = -1) -> np.ndarray:
  """Numerically stable softmax in numpy."""
  x = x - np.max(x, axis=axis, keepdims=True)
  e = np.exp(x)
  return e / np.sum(e, axis=axis, keepdims=True)



def resolve_dataset_path(dataset_path: Union[str, Path], base_dir: Optional[Union[str, Path]] = None) -> Path:
  """Resolve the dataset path robustly.

  This is useful when the script is executed from the package root but the
  dataset is stored inside ./datasets/... instead of the root folder.
  The function first respects the user-provided path. If it does not exist,
  it searches common NALU dataset locations and filenames recursively from
  the current working directory and the script directory.
  """
  requested = Path(dataset_path).expanduser()
  if requested.exists():
    return requested.resolve()

  search_roots: List[Path] = []
  if base_dir is not None:
    search_roots.append(Path(base_dir).expanduser().resolve())
  search_roots.append(Path.cwd().resolve())
  try:
    search_roots.append(Path(__file__).resolve().parent)
  except Exception:
    pass

  # Remove duplicates while preserving order.
  unique_roots: List[Path] = []
  seen = set()
  for root in search_roots:
    if root not in seen and root.exists():
      unique_roots.append(root)
      seen.add(root)

  requested_name = requested.name
  preferred_names = [
    requested_name,
    "NALU-PTBR.csv",
    "NALU-PTBR.csv",
    "NALU-PTBR.csv",
    "NALU-PTBR.csv",
    "NALU-PTBR.csv",
  ]

  matches: List[Path] = []
  for root in unique_roots:
    for name in preferred_names:
      try:
        matches.extend(root.rglob(name))
      except Exception:
        continue

  # Prefer psychometric, then exact requested name, then any candidate.
  if matches:
    def rank(path: Path) -> Tuple[int, int, str]:
      s = str(path).lower()
      if path.name == "NALU-PTBR.csv":
        priority = 0
      elif path.name == requested_name:
        priority = 1
      else:
        priority = 5
      return (priority, len(str(path)), str(path))
    best = sorted(set(matches), key=rank)[0]
    print(f"[INFO] Dataset path '{dataset_path}' was not found. Using detected dataset: {best}")
    return best.resolve()

  searched = ", ".join(str(r) for r in unique_roots)
  raise FileNotFoundError(
    f"Dataset not found: {dataset_path}\n"
    f"Searched from: {searched}\n"
    "Please pass the correct path, for example:\n"
    " --dataset ./datasets/nalu_psychometric/NALU-PTBR.csv"
  )


