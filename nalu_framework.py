  def _get_threshold(self, result: Dict[str, Any], head_name: str, label_name: str, default: float = 0.5) -> float:
    thresholds = result.get("thresholds_used", {}) or {}
    head_value = thresholds.get(head_name, default)
    if isinstance(head_value, dict):
      return float(head_value.get(label_name, default))
    return float(head_value)

  def _active_labels(self, result: Dict[str, Any], head_name: str, prob_map: Dict[str, float], min_floor: float) -> List[Tuple[str, float]]:
    active = []
    for label, score in sorted(prob_map.items(), key=lambda x: x[1], reverse=True):
      lookup = label if head_name != "emotion" else f"emotion_{label}"
      threshold = max(self._get_threshold(result, head_name, lookup, 0.5), min_floor)
      # For human-facing interpretation, do not require the stricter binary
      # classification threshold for all emotions. This improves summaries
      # without changing evaluation thresholds.
      if head_name == "emotion":
        emotion_floor = {
          "joy": 0.58, "sadness": 0.62, "anger": 0.65,
          "fear": 0.62, "disgust": 0.70, "surprise": 0.62,
          "calm": 0.62, "distress": 0.65, "neutral": 0.68,
        }.get(label, min_floor)
        threshold = max(min(threshold, 0.85), emotion_floor)
      if score >= threshold:
        active.append((label, score))
    return active

  def _suppress_contradictions(
    self,
    sentiment_probs: Dict[str, float],
    emotion_probs: Dict[str, float],
    psychological_probs: Dict[str, float],
    behavioral_probs: Dict[str, float],
    risk_probs: Dict[str, float],
    clinical_probs: Optional[Dict[str, float]] = None,
  ) -> Dict[str, Dict[str, float]]:
    clinical_probs = clinical_probs or {}

    adjusted = {
      "sentiment": dict(sentiment_probs),
      "emotion": dict(emotion_probs),
      "psychological": dict(psychological_probs),
      "behavioral": dict(behavioral_probs),
      "risk": dict(risk_probs),
    }

    positive_axis = float(np.mean([
      sentiment_probs.get("positive", 0.0),
      emotion_probs.get("joy", 0.0),
      psychological_probs.get("resilience_coping_cue", 0.0),
      behavioral_probs.get("coping_intent", 0.0),
    ]))
    negative_axis = float(np.mean([
      sentiment_probs.get("negative", 0.0),
      emotion_probs.get("sadness", 0.0),
      emotion_probs.get("distress", 0.0),
      psychological_probs.get("stress_pressure_cue", 0.0),
    ]))
    risk_axis = float(np.mean([
      risk_probs.get("mental_state_risk_flag", 0.0),
      risk_probs.get("crisis_danger_text", 0.0),
      psychological_probs.get("hopelessness_cue", 0.0),
      psychological_probs.get("loss_of_control_cue", 0.0),
      clinical_probs.get("panic_attack_language", 0.0),
    ]))
    protective_axis = float(np.mean([
      psychological_probs.get("resilience_coping_cue", 0.0),
      behavioral_probs.get("coping_intent", 0.0),
      behavioral_probs.get("seeking_help_intent", 0.0),
    ]))

    if positive_axis >= 0.60 and protective_axis >= 0.55 and risk_axis < 0.45:
      if adjusted["risk"].get("depression_related_language", 0.0) < 0.85:
        adjusted["risk"]["depression_related_language"] *= 0.20
      if adjusted["psychological"].get("hopelessness_cue", 0.0) < 0.80:
        adjusted["psychological"]["hopelessness_cue"] *= 0.20
      if adjusted["emotion"].get("sadness", 0.0) < 0.75:
        adjusted["emotion"]["sadness"] *= 0.35

    if negative_axis >= 0.55:
      adjusted["emotion"]["neutral"] *= 0.25
      adjusted["sentiment"]["neutral"] *= 0.40

    if protective_axis >= 0.50:
      if adjusted["behavioral"].get("hostility", 0.0) < 0.70:
        adjusted["behavioral"]["hostility"] *= 0.30
      if adjusted["behavioral"].get("toxicity", 0.0) < 0.70:
        adjusted["behavioral"]["toxicity"] *= 0.30

    if risk_axis < 0.35:
      adjusted["risk"]["crisis_danger_text"] *= 0.20
      adjusted["risk"]["mental_state_risk_flag"] *= 0.60

    return adjusted

  def interpret(self, result: Dict[str, Any]) -> Dict[str, Any]:
    text = str(result.get("text", "")).strip()
    raw_sentiment = result["sentiment_probs"]
    raw_emotion = result["emotion_probs"]
    raw_psychological = result["psychological_probs"]
    raw_behavioral = result["behavioral_probs"]
    raw_risk = result["risk_probs"]
    raw_clinical = result.get("clinical_probs", {})
    regression = result["regression"]
    psychometric = result.get("psychometric", {}) or {}

    adj = self._suppress_contradictions(
      raw_sentiment,
      raw_emotion,
      raw_psychological,
      raw_behavioral,
      raw_risk,
      raw_clinical,
    )
    sentiment_probs = adj["sentiment"]
    emotion_probs = adj["emotion"]
    psychological_probs = adj["psychological"]
    behavioral_probs = adj["behavioral"]
    risk_probs = adj["risk"]
    clinical_probs = dict(raw_clinical)

    valence = float(regression.get("valence_russell", 0.0))
    arousal = float(regression.get("arousal_russell", 0.0))

    def _psy_score(name: str, max_score: float) -> Tuple[float, float]:
      val = float(psychometric.get(name, 0.0))
      return val, val * max_score

    phq9_norm, phq9_raw = _psy_score("phq9_score_norm", 27.0)
    gad7_norm, gad7_raw = _psy_score("gad7_score_norm", 21.0)
    dass_d_norm, dass_d_raw = _psy_score("dass_depression_norm", 42.0)
    dass_a_norm, dass_a_raw = _psy_score("dass_anxiety_norm", 42.0)
    dass_s_norm, dass_s_raw = _psy_score("dass_stress_norm", 42.0)

    def _level_from_norm(x: float) -> str:
      if x >= 0.70:
        return "elevado"
      if x >= 0.45:
        return "moderado"
      if x >= 0.25:
        return "leve"
      return "baixo"

    psychometric_levels = {
      "phq9": _level_from_norm(phq9_norm),
      "gad7": _level_from_norm(gad7_norm),
      "dass_depression": _level_from_norm(dass_d_norm),
      "dass_anxiety": _level_from_norm(dass_a_norm),
      "dass_stress": _level_from_norm(dass_s_norm),
    }

    psychometric_summary_pt = (
      f"Estimativa psicométrica linguística: PHQ-9={phq9_raw:.1f}/27 ({psychometric_levels['phq9']}), "
      f"GAD-7={gad7_raw:.1f}/21 ({psychometric_levels['gad7']}), "
      f"DASS-D={dass_d_raw:.1f}/42 ({psychometric_levels['dass_depression']}), "
      f"DASS-A={dass_a_raw:.1f}/42 ({psychometric_levels['dass_anxiety']}), "
      f"DASS-S={dass_s_raw:.1f}/42 ({psychometric_levels['dass_stress']}). "
      "Estes valores são estimativas derivadas da linguagem e não equivalem a pontuações clínicas reais."
    )

    distress_index = float(np.mean([
      sentiment_probs.get("negative", 0.0),
      emotion_probs.get("distress", 0.0),
      emotion_probs.get("sadness", 0.0),
      psychological_probs.get("stress_pressure_cue", 0.0),
      risk_probs.get("anxiety_related_language", 0.0),
    ]))
    self_critical_index = float(np.mean([
      psychological_probs.get("self_devaluation_cue", 0.0),
      psychological_probs.get("guilt_shame_cue", 0.0),
      psychological_probs.get("hopelessness_cue", 0.0),
    ]))
    withdrawal_index = float(np.mean([
      psychological_probs.get("social_withdrawal_cue", 0.0),
      behavioral_probs.get("avoidance_intent", 0.0),
      psychological_probs.get("self_focus_cue", 0.0),
    ]))
    overload_index = float(np.mean([
      psychological_probs.get("stress_pressure_cue", 0.0),
      psychological_probs.get("cognitive_overload_cue", 0.0),
      psychological_probs.get("loss_of_control_cue", 0.0),
    ]))
    protective_index = float(np.mean([
      psychological_probs.get("resilience_coping_cue", 0.0),
      behavioral_probs.get("coping_intent", 0.0),
      behavioral_probs.get("seeking_help_intent", 0.0),
      psychological_probs.get("empathy_cue", 0.0),
    ]))
    risk_index = float(np.mean([
      risk_probs.get("mental_state_risk_flag", 0.0),
      risk_probs.get("crisis_danger_text", 0.0),
      risk_probs.get("depression_related_language", 0.0),
      psychological_probs.get("hopelessness_cue", 0.0),
      psychological_probs.get("loss_of_control_cue", 0.0),
      clinical_probs.get("panic_attack_language", 0.0),
    ]))

