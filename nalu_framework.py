class MetricsComputer:
  """Compute metrics, tune thresholds per label, and aggregate evaluation reports."""

  def __init__(self, schema: NALUSchema, threshold_grid: Sequence[float]):
    self.schema = schema
    self.threshold_grid = list(threshold_grid)

  @staticmethod
  def _safe_macro_f1(y_true: np.ndarray, y_pred: np.ndarray) -> float:
    return float(f1_score(y_true, y_pred, average="macro", zero_division=0))

  @staticmethod
  def _safe_micro_f1(y_true: np.ndarray, y_pred: np.ndarray) -> float:
    return float(f1_score(y_true, y_pred, average="micro", zero_division=0))

  @staticmethod
  def _default_thresholds(schema: NALUSchema) -> Dict[str, Dict[str, float]]:
    return {
      "emotion": {col: 0.5 for col in schema.emotion_cols},
      "psychological": {col: 0.5 for col in schema.psychological_cols},
      "behavioral": {col: 0.5 for col in schema.behavioral_cols},
      "risk": {col: 0.5 for col in schema.risk_cols},
      "clinical": {col: 0.5 for col in schema.clinical_cols},
    }

  def get_threshold_for_label(self, thresholds: Dict[str, Any], head_name: str, label_name: str, default: float = 0.5) -> float:
    if not thresholds:
      return default
    head_value = thresholds.get(head_name, default)
    if isinstance(head_value, dict):
      return float(head_value.get(label_name, default))
    return float(head_value)

  def tune_thresholds(
    self,
    val_targets: Dict[str, np.ndarray],
    val_probs: Dict[str, np.ndarray],
  ) -> Dict[str, Dict[str, float]]:
    """Tune one threshold per label using validation F1."""
    thresholds = self._default_thresholds(self.schema)

    for head_name in ["emotion", "psychological", "behavioral", "risk", "clinical"]:
      y_true = val_targets[head_name]
      y_prob = val_probs[head_name]
      cols = self.schema.all_heads[head_name]

      for i, col in enumerate(cols):
        yt = y_true[:, i]
        yp = y_prob[:, i]
        # If label is extremely sparse, keep threshold conservative and stable.
        if yt.sum() < 5:
          thresholds[head_name][col] = 0.60
          continue

        best_t = 0.50
        best_score = -1.0
        positive_prior = float(yt.mean())

        # Head-specific penalties avoid the previous issue where several
        # rare emotion labels were over-predicted even at threshold 0.80.
        penalty_by_head = {
          "emotion": 0.18,
          "psychological": 0.12,
          "behavioral": 0.12,
          "risk": 0.10,
          "clinical": 0.10,
        }
        balance_strength = penalty_by_head.get(head_name, 0.10)

        # Rare labels need good precision. For common labels we allow more recall.
        is_rare = positive_prior < 0.08
        is_common = positive_prior > 0.25

        for t in self.threshold_grid:
          pred = (yp >= t).astype(int)
          f1 = float(f1_score(yt, pred, zero_division=0))
          pred_rate = float(pred.mean())

          balance_penalty = abs(pred_rate - positive_prior) * balance_strength
          overprediction_penalty = max(0.0, pred_rate - max(positive_prior * 1.8, positive_prior + 0.025))
          underprediction_penalty = max(0.0, positive_prior * 0.35 - pred_rate) if not is_rare else 0.0

          score = f1 - balance_penalty - 0.10 * overprediction_penalty - 0.04 * underprediction_penalty

          # Prefer slightly more conservative thresholds when scores are virtually tied.
          if score > best_score + 1e-6 or (abs(score - best_score) <= 1e-6 and t > best_t):
            best_score = score
            best_t = float(t)

        if head_name == "emotion":
          # Avoid extremely low thresholds for ambiguous emotion labels.
          if is_rare:
            best_t = max(best_t, 0.60)
          elif not is_common:
            best_t = max(best_t, 0.50)

        thresholds[head_name][col] = best_t

    return thresholds

  def compute(
    self,
    targets: Dict[str, np.ndarray],
    probs: Dict[str, np.ndarray],
    thresholds: Dict[str, Any],
  ) -> Dict[str, Any]:
    report: Dict[str, Any] = {}

    sentiment_true = targets["sentiment"]
    sentiment_prob = probs["sentiment"]
    sentiment_pred = np.argmax(sentiment_prob, axis=1)
    report["sentiment"] = {
      "accuracy": float(accuracy_score(sentiment_true, sentiment_pred)),
      "macro_f1": float(f1_score(sentiment_true, sentiment_pred, average="macro", zero_division=0)),
      "weighted_f1": float(f1_score(sentiment_true, sentiment_pred, average="weighted", zero_division=0)),
    }

    for head_name in ["emotion", "psychological", "behavioral", "risk", "clinical"]:
      y_true = targets[head_name]
      y_prob = probs[head_name]
      cols = self.schema.all_heads[head_name]

      y_pred = np.zeros_like(y_true)
      per_label = {}

      for i, col in enumerate(cols):
        t = self.get_threshold_for_label(thresholds, head_name, col, 0.5)
        y_pred[:, i] = (y_prob[:, i] >= t).astype(int)
        per_label[col] = {
          "threshold": float(t),
          "positive_true": int(y_true[:, i].sum()),
          "positive_pred": int(y_pred[:, i].sum()),
          "f1": float(f1_score(y_true[:, i], y_pred[:, i], zero_division=0)),
        }

      report[head_name] = {
        "macro_f1": self._safe_macro_f1(y_true, y_pred),
        "micro_f1": self._safe_micro_f1(y_true, y_pred),
        "hamming_loss": float(hamming_loss(y_true, y_pred)),
        "avg_cardinality_true": float(y_true.sum(axis=1).mean()),
        "avg_cardinality_pred": float(y_pred.sum(axis=1).mean()),
        "per_label": per_label,
      }

    y_true_reg = targets["regression"]
    y_pred_reg = probs["regression"]
    reg_report = {}
    for i, col in enumerate(self.schema.regression_cols):
      mae = float(mean_absolute_error(y_true_reg[:, i], y_pred_reg[:, i]))
      rmse = float(math.sqrt(mean_squared_error(y_true_reg[:, i], y_pred_reg[:, i])))
      corr = None
      if pearsonr is not None and len(y_true_reg) > 1:
        try:
          corr = float(pearsonr(y_true_reg[:, i], y_pred_reg[:, i])[0])
        except Exception:
          corr = None
      reg_report[col] = {"mae": mae, "rmse": rmse, "pearson": corr}
    report["regression"] = reg_report

    psych_report = {}
    if "psychometric" in targets and "psychometric" in probs:
      y_true_psy = targets["psychometric"]
      y_pred_psy = probs["psychometric"]
      mask = targets.get("psychometric_mask", np.ones((len(y_true_psy),), dtype=float)).astype(float) >= 0.5
      if mask.sum() > 1:
        for i, col in enumerate(self.schema.psychometric_cols):
          yt = y_true_psy[mask, i]
          yp = y_pred_psy[mask, i]
          mae = float(mean_absolute_error(yt, yp))
          rmse = float(math.sqrt(mean_squared_error(yt, yp)))
          corr = None
          if pearsonr is not None and len(yt) > 1:
            try:
              corr = float(pearsonr(yt, yp)[0])
            except Exception:
              corr = None
          psych_report[col] = {"mae": mae, "rmse": rmse, "pearson": corr, "n_valid": int(mask.sum())}
    report["psychometric"] = psych_report

    # Emotion receives extra weight because it was the main bottleneck in the
    # previous run. Risk/clinical are also slightly prioritised for the final
    # checkpoint selection.
    regression_score = 1.0 / (1.0 + np.mean([report["regression"][c]["mae"] for c in self.schema.regression_cols]))
    if report.get("psychometric"):
      psychometric_score = 1.0 / (1.0 + np.mean([v["mae"] for v in report["psychometric"].values()]))
      psych_weight = 0.45
    else:
      psychometric_score = 0.0
      psych_weight = 0.0
    report["composite_score"] = float(
      1.00 * report["sentiment"]["macro_f1"]
      + 1.60 * report["emotion"]["macro_f1"]
      + 1.10 * report["psychological"]["macro_f1"]
      + 1.00 * report["behavioral"]["macro_f1"]
      + 1.15 * report["risk"]["macro_f1"]
      + 1.15 * report["clinical"]["macro_f1"]
      + 0.80 * regression_score
      + psych_weight * psychometric_score
    ) / (7.80 + psych_weight)
    return report


