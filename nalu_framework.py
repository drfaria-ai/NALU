  weight_decay: float = 0.01
  warmup_ratio: float = 0.1
  grad_clip: float = 1.0
  gradient_accumulation_steps: int = 1
  dropout: float = 0.2

  train_size: float = 0.80
  val_size: float = 0.10
  test_size: float = 0.10

  regression_loss: str = "smooth_l1" # smooth_l1 or mse

  # Slightly up-weight the emotion branch because it is the hardest head and
  # has the largest effect on the final interpretive quality.
  lambda_sentiment: float = 1.0
  lambda_emotion: float = 1.35
  lambda_psychological: float = 1.05
  lambda_behavioral: float = 1.0
  lambda_risk: float = 1.10
