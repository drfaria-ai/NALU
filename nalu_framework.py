class AsymmetricFocalMultilabelLoss(nn.Module):
  """Asymmetric focal loss for imbalanced multi-label NLP heads.

  This is more robust than plain BCE for sparse labels because it down-weights
  easy negatives while still preserving positive evidence through pos_weight.
  It is especially useful for rare emotion labels such as disgust/fear and
  rare clinical/risk labels.
  """

  def __init__(
    self,
    pos_weight: Optional[torch.Tensor] = None,
    gamma_pos: float = 0.0,
    gamma_neg: float = 2.0,
    clip: float = 0.05,
    eps: float = 1e-8,
  ):
    super().__init__()
    self.gamma_pos = float(gamma_pos)
    self.gamma_neg = float(gamma_neg)
    self.clip = float(clip)
    self.eps = float(eps)
    if pos_weight is not None:
      self.register_buffer("pos_weight", pos_weight.detach().clone().float())
    else:
      self.pos_weight = None

  def forward(self, logits: torch.Tensor, targets: torch.Tensor) -> torch.Tensor:
    targets = targets.float()
    probs = torch.sigmoid(logits)
    pos_probs = probs
    neg_probs = 1.0 - probs

    if self.clip > 0:
      neg_probs = (neg_probs + self.clip).clamp(max=1.0)

    pos_loss = targets * torch.log(pos_probs.clamp_min(self.eps))
    neg_loss = (1.0 - targets) * torch.log(neg_probs.clamp_min(self.eps))

    if self.gamma_pos > 0 or self.gamma_neg > 0:
      pos_weight = torch.pow(1.0 - pos_probs, self.gamma_pos)
      neg_weight = torch.pow(probs, self.gamma_neg)
      pos_loss = pos_loss * pos_weight
      neg_loss = neg_loss * neg_weight

    if self.pos_weight is not None:
      pw = self.pos_weight.to(logits.device, dtype=logits.dtype).view(1, -1)
      pos_loss = pos_loss * pw

    loss = -(pos_loss + neg_loss)
    return loss.mean()


class MultiHeadLoss(nn.Module):
  """Weighted multi-task loss with CE, BCE, and regression losses."""

  def __init__(
    self,
    config: TrainConfig,
    sentiment_class_weights: Optional[torch.Tensor] = None,
    emotion_pos_weight: Optional[torch.Tensor] = None,
    psychological_pos_weight: Optional[torch.Tensor] = None,
    behavioral_pos_weight: Optional[torch.Tensor] = None,
    risk_pos_weight: Optional[torch.Tensor] = None,
    clinical_pos_weight: Optional[torch.Tensor] = None,
  ):
    super().__init__()
    self.config = config

    self.sentiment_loss_fn = nn.CrossEntropyLoss(weight=sentiment_class_weights)

    def _scaled_pos_weight(pos_weight: Optional[torch.Tensor]) -> Optional[torch.Tensor]:
      if pos_weight is None:
        return None
      # Softer than raw neg/pos to avoid excessive over-prediction of rare labels.
      return torch.pow(pos_weight.clamp_min(1.0), float(config.focal_pos_weight_power))

    if config.use_asymmetric_focal_loss:
      self.emotion_loss_fn = AsymmetricFocalMultilabelLoss(
        _scaled_pos_weight(emotion_pos_weight),
        gamma_pos=config.focal_gamma_pos,
        gamma_neg=config.focal_gamma_neg,
        clip=config.focal_clip,
      )
      self.psychological_loss_fn = AsymmetricFocalMultilabelLoss(
        _scaled_pos_weight(psychological_pos_weight),
        gamma_pos=config.focal_gamma_pos,
        gamma_neg=max(1.5, config.focal_gamma_neg - 0.25),
        clip=config.focal_clip,
      )
      self.behavioral_loss_fn = AsymmetricFocalMultilabelLoss(
        _scaled_pos_weight(behavioral_pos_weight),
        gamma_pos=config.focal_gamma_pos,
        gamma_neg=config.focal_gamma_neg,
        clip=config.focal_clip,
      )
      self.risk_loss_fn = AsymmetricFocalMultilabelLoss(
        _scaled_pos_weight(risk_pos_weight),
        gamma_pos=config.focal_gamma_pos,
        gamma_neg=max(1.5, config.focal_gamma_neg - 0.25),
        clip=config.focal_clip,
      )
      self.clinical_loss_fn = AsymmetricFocalMultilabelLoss(
        _scaled_pos_weight(clinical_pos_weight),
        gamma_pos=config.focal_gamma_pos,
        gamma_neg=max(1.5, config.focal_gamma_neg - 0.25),
        clip=config.focal_clip,
      )
    else:
      self.emotion_loss_fn = nn.BCEWithLogitsLoss(pos_weight=emotion_pos_weight)
      self.psychological_loss_fn = nn.BCEWithLogitsLoss(pos_weight=psychological_pos_weight)
      self.behavioral_loss_fn = nn.BCEWithLogitsLoss(pos_weight=behavioral_pos_weight)
      self.risk_loss_fn = nn.BCEWithLogitsLoss(pos_weight=risk_pos_weight)
      self.clinical_loss_fn = nn.BCEWithLogitsLoss(pos_weight=clinical_pos_weight)

    if config.regression_loss == "mse":
      self.regression_loss_fn = nn.MSELoss()
    else:
      self.regression_loss_fn = nn.SmoothL1Loss()
    self.psychometric_loss_fn = nn.SmoothL1Loss(reduction="none")

  def forward(self, outputs: Dict[str, torch.Tensor], batch: Dict[str, torch.Tensor]) -> Tuple[torch.Tensor, Dict[str, float]]:
    """Compute total weighted loss."""
    sentiment_loss = self.sentiment_loss_fn(outputs["sentiment_logits"], batch["sentiment"])
    emotion_loss = self.emotion_loss_fn(outputs["emotion_logits"], batch["emotion"])
    psychological_loss = self.psychological_loss_fn(outputs["psychological_logits"], batch["psychological"])
    behavioral_loss = self.behavioral_loss_fn(outputs["behavioral_logits"], batch["behavioral"])
    risk_loss = self.risk_loss_fn(outputs["risk_logits"], batch["risk"])
    clinical_loss = self.clinical_loss_fn(outputs["clinical_logits"], batch["clinical"])
    regression_loss = self.regression_loss_fn(outputs["regression"], batch["regression"])

    # Psychometric loss is computed only for rows with valid psychometric supervision.
    psych_raw = self.psychometric_loss_fn(outputs["psychometric"], batch["psychometric"]).mean(dim=1)
    psych_mask = batch.get("psychometric_mask", torch.zeros_like(psych_raw)).float()
    psychometric_loss = (psych_raw * psych_mask).sum() / psych_mask.sum().clamp_min(1.0)

    total = (
      self.config.lambda_sentiment * sentiment_loss
      + self.config.lambda_emotion * emotion_loss
      + self.config.lambda_psychological * psychological_loss
      + self.config.lambda_behavioral * behavioral_loss
      + self.config.lambda_risk * risk_loss
      + self.config.lambda_clinical * clinical_loss
      + self.config.lambda_regression * regression_loss
      + self.config.lambda_psychometric * psychometric_loss
    )

    loss_dict = {
      "loss_total": float(total.detach().cpu().item()),
      "loss_sentiment": float(sentiment_loss.detach().cpu().item()),
      "loss_emotion": float(emotion_loss.detach().cpu().item()),
      "loss_psychological": float(psychological_loss.detach().cpu().item()),
      "loss_behavioral": float(behavioral_loss.detach().cpu().item()),
      "loss_risk": float(risk_loss.detach().cpu().item()),
      "loss_clinical": float(clinical_loss.detach().cpu().item()),
      "loss_regression": float(regression_loss.detach().cpu().item()),
      "loss_psychometric": float(psychometric_loss.detach().cpu().item()),
    }
    return total, loss_dict


