  # Wider threshold range is important: earlier runs saturated at 0.80 for many
  # labels, causing over-prediction in disgust/distress/neutral.
  threshold_grid: Tuple[float, ...] = tuple(np.round(np.arange(0.10, 0.96, 0.05), 2))

  # Improved multilabel loss. ASL/focal behaviour helps rare emotion and clinical labels.
  use_asymmetric_focal_loss: bool = True
  focal_gamma_pos: float = 0.0
  focal_gamma_neg: float = 2.0
  focal_clip: float = 0.05
  focal_pos_weight_power: float = 0.50

  save_best_metric: str = "composite_score"
  early_stopping_patience: int = 2

  num_workers: int = 0 # safer across platforms
  pin_memory: bool = False

  use_llm_rewriter: bool = False
  llm_model_name: str = ""
  llm_max_new_tokens: int = 180
  resume_from_checkpoint: str = ""

  def to_json(self, path: Union[str, Path]) -> None:
    """Save config."""
    json_dump(asdict(self), path)

