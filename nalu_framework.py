  def _save_checkpoint(
    self,
    name: str,
    thresholds: Dict[str, float],
    metrics: Dict[str, Any],
  ) -> None:
    """Save model, tokenizer, config, thresholds, and metrics."""
    ckpt = ensure_dir(self.checkpoint_dir / name)
    torch.save(self.model.state_dict(), ckpt / "model.pt")
    self.tokenizer.save_pretrained(ckpt / "tokenizer")
    self.config.to_json(ckpt / "train_config.json")
    json_dump(thresholds, ckpt / "thresholds.json")
    json_dump(metrics, ckpt / "metrics.json")
    json_dump(self._interpretation_calibration_config(), ckpt / "interpretation_calibration_config.json")

  def load_checkpoint(self, checkpoint_dir: Union[str, Path]) -> None:
    """Load trained state, tokenizer, config, and thresholds."""
    checkpoint_dir = Path(checkpoint_dir)
    cfg_path = checkpoint_dir / "train_config.json"
    if cfg_path.exists():
      self.config = TrainConfig.from_json(cfg_path)

    tokenizer_dir = checkpoint_dir / "tokenizer"
    if tokenizer_dir.exists():
      self.tokenizer = AutoTokenizer.from_pretrained(tokenizer_dir)

    self.model = MultiHeadBertimbau(
      model_name=self.config.pretrained_model_name,
      schema=self.schema,
      dropout=self.config.dropout,
      gradient_checkpointing=False,
    ).to(self.device)

    state = torch.load(checkpoint_dir / "model.pt", map_location=self.device)
    load_result = self.model.load_state_dict(state, strict=False)
    if getattr(load_result, "missing_keys", None):
      print(f"Checkpoint loaded with missing keys initialized randomly: {load_result.missing_keys}")
    if getattr(load_result, "unexpected_keys", None):
      print(f"Checkpoint loaded with unexpected keys ignored: {load_result.unexpected_keys}")
    self.model.eval()

    thresholds_path = checkpoint_dir / "thresholds.json"
    if thresholds_path.exists():
      self.best_thresholds = json_load(thresholds_path)
    else:
      self.best_thresholds = self.metrics_computer._default_thresholds(self.schema)

    metrics_path = checkpoint_dir / "metrics.json"
    if metrics_path.exists():
      self.best_metrics = json_load(metrics_path)

