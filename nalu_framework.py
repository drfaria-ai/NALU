def prepare_nalu_ptbr_datasets(base_dataset: Union[str, Path], mentalchat_zip: Union[str, Path], output_dir: Union[str, Path]) -> Dict[str, str]:
  """Create NALU-PTBR datasets from a compatible base dataset plus MentalChat16K."""
  output_dir = ensure_dir(output_dir)
  schema = NALUSchema()

  base_df = pd.read_csv(base_dataset)
  base_nalu = add_clinical_cues_to_nalu_dataframe(base_df, schema)
  ordered_cols = [schema.id_col, schema.text_col] + schema.all_label_cols
  base_nalu = base_nalu[ordered_cols].copy()

  extract_dir = ensure_dir(Path(output_dir) / "mentalchat16k_extracted")
  with zipfile.ZipFile(mentalchat_zip) as z:
    z.extractall(extract_dir)
  json_candidates = list(Path(extract_dir).glob("*MentalChat16K*train*.json")) + list(Path(extract_dir).glob("*.json"))
  json_candidates = [p for p in json_candidates if p.name != "__huggingface_repos__.json"]
  if not json_candidates:
    raise FileNotFoundError("Could not find MentalChat16K JSON file inside the zip.")
  mentalchat_json = json_candidates[0]

  reference_df = mentalchat16k_reference_to_silver_dataframe(mentalchat_json)
  synthetic_df = create_nalu_synthetic_clinical_refinement(schema)
  train_df = pd.concat([base_nalu, synthetic_df], ignore_index=True)

  base_nalu_path = Path(output_dir) / "NALU-PTBR.csv"
  reference_path = Path(output_dir) / "MentalChat16K_NALU_EN_silver_reference.csv"
  synthetic_path = Path(output_dir) / "NALU_MentalChat16K_PT_clinical_refinement.csv"
  train_path = Path(output_dir) / "NALU-PTBR.csv"
  summary_path = Path(output_dir) / "NALU_dataset_summary.json"

  base_nalu.to_csv(base_nalu_path, index=False, encoding="utf-8")
  reference_df.to_csv(reference_path, index=False, encoding="utf-8")
  synthetic_df.to_csv(synthetic_path, index=False, encoding="utf-8")
  train_df.to_csv(train_path, index=False, encoding="utf-8")

  summary = {
    "base_rows": int(len(base_nalu)),
    "synthetic_refinement_rows": int(len(synthetic_df)),
    "final_train_rows": int(len(train_df)),
    "mentalchat_reference_rows": int(len(reference_df)),
    "clinical_positive_counts_final_train": {col: int(train_df[col].sum()) for col in schema.clinical_cols},
    "files": {
      "base_nalu": str(base_nalu_path),
      "mentalchat_reference": str(reference_path),
      "synthetic_refinement": str(synthetic_path),
      "train": str(train_path),
    },
  }
  json_dump(summary, summary_path)
  return {**summary["files"], "summary": str(summary_path)}

